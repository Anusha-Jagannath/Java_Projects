package typecasting;

public class TypeCasting {

    public static void main(String args[]) {
        System.out.println("Type casting lesson");
        byte x = 4;
        int y = x;
        System.out.println("The value of y is "+x);

        int a = 4;
        double d = a;
        System.out.println("The value of d is "+x);

        double r = 3.62;
        int u = (int)r;
        System.out.println("The value of u is "+u);
    }
}
