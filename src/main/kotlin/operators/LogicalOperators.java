package operators;

public class LogicalOperators {

    public static void main(String args[]) {
        System.out.println("Logical operators");
        boolean a = true;
        boolean b = false;
        boolean result = a && b;
        boolean result2 = a || b;
        boolean result3 = !b;
        System.out.println(result);
        System.out.println(result2);
        System.out.println(result3);
    }
}

